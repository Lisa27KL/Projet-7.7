module.exports = (sequelize, Sequelize) => {
    const Comments = sequelize.define("comments", {
      message: {
        type: Sequelize.STRING,
        allowNull: false
      },
      createdAt: {
        allowNull: false,
        type: Sequelize.DATE
      },
      updatedAt: {
        allowNull: false,
        type: Sequelize.DATE
      }
    });
    return Comments;
  };